const maxSum = (arr, length) => {
    if(arr.length < length){
        return "array is not valid"
    }
    let sum = 0;
    for (let i = 0; i < length; i++) {
        sum += arr[i]
    }
    for (let i = length; i < arr.length; i++) {
        let newSum = sum + arr[i] - arr[(i - length)]
        if(newSum > sum){
            sum = newSum
        }
    }
    return sum
}

